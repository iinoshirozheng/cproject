#„ÄêÂ∑≤‰øÆÊîπ„ÄëÈáçÊßãÊåá‰ª§ÂàÜÊ¥æÂô®‰ª•ÊîØÊè¥ÂèÉÊï∏
# --- ‰∏ªÈÇèËºØ ---
if [[ $# -lt 1 ]]; then
    echo "‚ö†Ô∏è  Ë´ãÊèê‰æõ‰∏ÄÂÄãÂëΩ‰ª§„ÄÇ" >&2
    usage
