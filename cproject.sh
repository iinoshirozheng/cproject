        exit 1
    fi

    echo "üöÄ Âü∑Ë°å‰∏ªÁ®ãÂºè (${build_config} ÁµÑÊÖã)..."
    echo "------------------------------------------"
    "${executable_path}"
    echo "------------------------------------------"
    echo "‚úÖ Á®ãÂºèÂü∑Ë°åÂÆåÁï¢„ÄÇ"
}

copy_artifacts() {
    local project_name="$1"
    local project_dir="$2"
    local build_dir="$3"
    local lib_dir="$4"

    local lib_candidates=()
    while IFS= read -r -d '' f; do
        case "$f" in */CMakeFiles/*|*/tests/*|*/test/*) continue ;; esac
        lib_candidates+=("$f")
    done < <(find "${build_dir}" -type f \( -name "lib${project_name}.a" -o -name "lib${project_name}.so" -o -name "lib${project_name}.dylib" -o -name "${project_name}.lib" \) -print0)

    if (( ${#lib_candidates[@]} > 0 )); then
        echo "üì¶ Ê≠£Âú®ËôïÁêÜÂáΩÂºèÂ∫´Áî¢Âá∫..."
        rm -rf "${lib_dir}"
        mkdir -p "${lib_dir}"
        for f in "${lib_candidates[@]}"; do rsync -a "$f" "${lib_dir}/"; done
        if [[ -d "${project_dir}/include" ]]; then
            rsync -a --delete "${project_dir}/include/" "${lib_dir}/include/"
        fi
        echo "‚úÖ ÂáΩÂºèÂ∫´Ë§áË£ΩÂÆåÊàê„ÄÇ"
    else
        : # Do nothing
    fi
}

do_pkg_search() {
    local lib_name="$1"
    if [[ -z "$lib_name" ]]; then
        echo "‚ùå ÈåØË™§ÔºöË´ãÊèê‰æõË¶ÅÊêúÂ∞ãÁöÑÂáΩÂºèÂ∫´ÂêçÁ®±„ÄÇ" >&2
        echo "   Áî®Ê≥ï: cproject pkg search <lib-name>" >&2
        exit 1
    fi
    echo "üîé Ê≠£Âú®ÈÄèÈÅé vcpkg search ÊêúÂ∞ã '${lib_name}'..."
    vcpkg search "$lib_name"
}

do_pkg_add() {
    local lib_name="$1"

    if ! command -v vcpkg &> /dev/null; then echo "‚ùå ÈåØË™§ÔºöÊâæ‰∏çÂà∞ 'vcpkg' Êåá‰ª§„ÄÇ" >&2; exit 1; fi
    if [[ -z "$lib_name" ]]; then
        echo "‚ùå ÈåØË™§ÔºöË´ãÊèê‰æõÂáΩÂºèÂ∫´ÂêçÁ®±„ÄÇ" >&2
        echo "   Áî®Ê≥ï: cproject pkg add <lib-name>" >&2
        exit 1
    fi
    local cmake_deps_file="cmake/dependencies.cmake"
    if [[ ! -f "$cmake_deps_file" ]]; then echo "‚ùå ÈåØË™§ÔºöÊâæ‰∏çÂà∞ cmake/dependencies.cmake" >&2; exit 1; fi

    if [[ "$lib_name" == "gtest" ]]; then
        echo "üì¶ Ê≠£Âú®ÂÆâË£ùÈ†êË®≠ÁöÑÊ∏¨Ë©¶Ê°ÜÊû∂ gtest Ëàá gmock..."
        vcpkg install gtest gmock
        echo "‚úÖ gtest Ëàá gmock Â∑≤ÂÆâË£ù„ÄÇ"
        return 0
    fi

    if grep -q "# === ${lib_name} START ===" "${cmake_deps_file}"; then
        echo "‚ÑπÔ∏è  Â•ó‰ª∂ '${lib_name}' ÁöÑË®≠ÂÆöÂ∑≤Â≠òÂú®Êñº ${cmake_deps_file} ‰∏≠„ÄÇ"
        return 0
    fi

    echo "üì¶ Ê≠£Âú®ÂÆâË£ù '${lib_name}' Âà∞ vcpkg..."
    vcpkg install "$lib_name"

    echo "‚öôÔ∏è Ê≠£Âú®Ëß£Êûê CMake Áî®Ê≥ï..."
    local find_package_line=""
    local link_targets=""

    if ! command -v jq &> /dev/null; then echo "‚ö†Ô∏è  Ë≠¶ÂëäÔºöÂª∫Ë≠∞ÂÆâË£ù 'jq' ‰ª•Áç≤ÂæóÊõ¥Á≤æÊ∫ñÁöÑ CMake Ë®≠ÂÆö„ÄÇ"; fi
    
    if pkg_info_json="$(vcpkg x-package-info "$lib_name" --x-json 2>/dev/null)"; then
        find_package_line="$(printf "%s" "$pkg_info_json" | jq -r '.usage.cmake.find_package // empty')"
        link_targets="$(printf "%s" "$pkg_info_json" | jq -r '[.usage.cmake.targets[]?] | join(" ")')"
    fi

    if [[ -z "$find_package_line" || -z "$link_targets" ]]; then
        local cmake_pkg; cmake_pkg="$(printf "%s" "$lib_name" | tr '-' '_')"
        find_package_line="find_package(${cmake_pkg} CONFIG REQUIRED)"
        link_targets="${cmake_pkg}::${cmake_pkg}"
        echo "‚ÑπÔ∏è vcpkg x-package-info ‰∏çÂèØÁî®ÊàñÂõûÂÇ≥Á©∫ÔºåÂ∑≤Â•óÁî® fallback„ÄÇ"
    fi
    
    echo "üìù Ê≠£Âú®Êõ¥Êñ∞ ${cmake_deps_file}..."
    {
        echo ""
        echo "# === ${lib_name} START ==="
        echo "${find_package_line}"
        echo "list(APPEND THIRD_PARTY_LIBS ${link_targets})"
        echo "# === ${lib_name} END ==="
    } >> "${cmake_deps_file}"

    echo "‚úÖ ÊàêÂäüÊñ∞Â¢ûÂ•ó‰ª∂ '${lib_name}'ÔºÅ"
}

do_pkg_rm() {
    local lib_name="$1"
    
    if [[ -z "$lib_name" ]]; then echo "‚ùå ÈåØË™§ÔºöË´ãÊèê‰æõÂáΩÂºèÂ∫´ÂêçÁ®±„ÄÇ" >&2; echo "   Áî®Ê≥ï: cproject pkg rm <lib-name>" >&2; exit 1; fi
    local cmake_deps_file="cmake/dependencies.cmake"
    if [[ ! -f "${cmake_deps_file}" ]]; then echo "‚ùå ÈåØË™§ÔºöÊâæ‰∏çÂà∞ cmake/dependencies.cmake" >&2; exit 1; fi
    
    if [[ "$lib_name" == "gtest" || "$lib_name" == "gmock" ]]; then
        echo "‚ö†Ô∏è  gtest/gmock ÊòØÂ∞àÊ°àÂü∫Á§é‰æùË≥¥Ôºå‰∏çÂª∫Ë≠∞ÁßªÈô§„ÄÇËã•Ë¶ÅÁßªÈô§ÔºåË´ãÊâãÂãï‰øÆÊîπ cmake/gtest.cmake„ÄÇ"
        return 1
    fi

    echo "üóëÔ∏è  Ê≠£Âú®Âæû vcpkg ‰∏≠ÁßªÈô§ '${lib_name}'..."
    vcpkg remove --purge "$lib_name"

    if grep -q "# === ${lib_name} START ===" "${cmake_deps_file}"; then
        echo "üìù Ê≠£Âú®Âæû ${cmake_deps_file} ‰∏≠ÁßªÈô§ '${lib_name}' ÁöÑË®≠ÂÆö..."
        sed -i.bak "/# === ${lib_name} START ===/,/# === ${lib_name} END ===/d" "${cmake_deps_file}"
        rm -f "${cmake_deps_file}.bak"
    else
        echo "‚ÑπÔ∏è Âú® ${cmake_deps_file} ‰∏≠Êâæ‰∏çÂà∞ '${lib_name}' ÁöÑË®≠ÂÆöÂçÄÂ°ä„ÄÇ"
    fi

    echo "‚úÖ ÊàêÂäüÁßªÈô§‰æùË≥¥ '${lib_name}'ÔºÅ"
}


