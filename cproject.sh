include(GoogleTest)
set(TEST_LIBS GTest::gtest GTest::gtest_main GTest::gmock GTest::gmock_main)
EOF

    echo "üìù Ê≠£Âú®Áî¢Áîü‰∏ª CMakeLists.txt..."
    if [ "${PROJECT_TYPE}" == "library" ]; then
        cat > "${PROJECT_DIR}/CMakeLists.txt" <<EOF
