SUBCMD="$1"; shift

case "$SUBCMD" in
    create)
        do_create "$@"
        ;;
    
    build)
        build_config="release" # Default
        while [[ "$#" -gt 0 ]]; do
            case $1 in
                -d|--debug) build_config="debug"; shift ;;
                -r|--release) build_config="release"; shift ;;
                *) echo "‚ùå build ÁöÑÊú™Áü•ÂèÉÊï∏: $1" >&2; usage; exit 1 ;;
            esac
        done
        do_build "$build_config"
        ;;

    run)
        build_config="release" # Default
        while [[ "$#" -gt 0 ]]; do
            case $1 in
                -d|--debug) build_config="debug"; shift ;;
                -r|--release) build_config="release"; shift ;;
                *) echo "‚ùå run ÁöÑÊú™Áü•ÂèÉÊï∏: $1" >&2; usage; exit 1 ;;
            esac
        done
        do_run "$build_config"
        ;;

    test)
        do_test
        ;;
    
    add)
        do_pkg_add "$@"
        ;;
    remove)
        do_pkg_rm "$@"
        ;;
    search)
        do_pkg_search "$@"
        ;;
    
    *)
        echo "‚ùå Êú™Áü•ÂëΩ‰ª§: $SUBCMD" >&2
        usage
        ;;
esac